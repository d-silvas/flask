from db import db


class StoreModel(db.Model):
    __tablename__ = 'stores'
    id = db.Column(db.Integer, primary_key=True)
    name = db.Column(db.String(80))

    # lazy='dynamic' transforms "items" into a query builder, and avoids
    # loading all the items in the database that belong to this StoreModel
    items = db.relationship('ItemModel', lazy='dynamic')

    def __init__(self, name):
        self.name = name

    def json(self):
        return {'name': self.name, 'items': [item.json() for item in self.items.all()]}

    @classmethod
    # https://mypy.readthedocs.io/en/stable/kinds_of_types.html#class-name-forward-references
    def find_by_name(cls, name) -> 'StoreModel':
        return cls.query.filter_by(name=name).first()

    def save_to_db(self):
        db.session.add(self)
        db.session.commit()

    def delete_from_db(self):
        db.session.delete(self)
        db.session.commit()
